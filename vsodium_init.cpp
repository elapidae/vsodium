#include "vsodium_init.h"

#include <sodium.h>
#include <stdexcept>

//=======================================================================================
static int inited = 0;
//=======================================================================================
vsodium_init::vsodium_init()
{
    if (inited++) return;

    if ( 0 != sodium_init() )
    {
        throw std::runtime_error( "sodium_init()" );
    }
}
//=======================================================================================
vsodium_init::~vsodium_init()
{}
//=======================================================================================
